#include <stdio.h>

int main() {
    char username[50], password[50];
    char userToReport[50];
    int duration, numBots;

    printf("Welcome to ReportLikee Tool\n");
    printf("Enter your username: ");
    scanf("%s", username);
    printf("Enter your password: ");
    scanf("%s", password);

    printf("Choose the user to report: ");
    scanf("%s", userToReport);
    printf("Enter the duration to report (in seconds): ");
    scanf("%d", &duration);
    printf("Enter the number of bots (10-300): ");
    scanf("%d", &numBots);

    printf("\nReportLikee Tool - Warning: Use responsibly!\n");
    printf("Reporting %s for %d seconds with %d bots.\n", userToReport, duration, numBots);

    while (1) {
        // Report userToReport with numBots for duration seconds
        printf("User %s reported for %d seconds with %d bots.\n", userToReport, duration, numBots);
    }

    return 0;
}
